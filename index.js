// Подключаем фреймворк Express
const express = require('express');

// Создаем приложение
const app = express();

// Устанавливаем порт. Render предоставит свой, для локального теста будет 3000
const PORT = process.env.PORT || 3000;

// Главный маршрут. Отвечает на запросы к корню сайта
app.get('/', (req, res) => {
  res.send('Сервер Gifty работает. Основа заложена.');
});

// Запускаем сервер и сообщаем об этом в консоль
app.listen(PORT, () => {
  console.log(`Сервер запущен на порту ${PORT}`);
});
